package com.smartech.invoicing.dto;

public class ResponsiveLetterLines {

	private String assetName;
	private String assetDescription;
	private String assetBrand;
	private String assetSerial;
	private String assetQuantity;
	public String getAssetName() {
		return assetName;
	}
	public void setAssetName(String assetName) {
		this.assetName = assetName;
	}
	public String getAssetDescription() {
		return assetDescription;
	}
	public void setAssetDescription(String assetDescription) {
		this.assetDescription = assetDescription;
	}
	public String getAssetBrand() {
		return assetBrand;
	}
	public void setAssetBrand(String assetBrand) {
		this.assetBrand = assetBrand;
	}
	public String getAssetSerial() {
		return assetSerial;
	}
	public void setAssetSerial(String assetSerial) {
		this.assetSerial = assetSerial;
	}
	public String getAssetQuantity() {
		return assetQuantity;
	}
	public void setAssetQuantity(String assetQuantity) {
		this.assetQuantity = assetQuantity;
	}
	
}
